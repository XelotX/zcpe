$a = 5;
$b = 2;

// ADDING
$c = $a + $b;
echo "a+b=$c" . PHP_EOL; // a+b=7

// SUBSTRACTING
$d = $a - $b;
echo "a-b=$d" . PHP_EOL; // a-b=3

// MULTIPLYING
$e = $a * $b;
echo "a*b=$e" . PHP_EOL; // a*b=10

// DIVIDING
$f = $a / $b;
echo "a/b=$f" . PHP_EOL; // a/b=2.5

// MODULUS
$g = $a % $b;
echo "a%b=$g" . PHP_EOL; // a%b=1
